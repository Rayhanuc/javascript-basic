// Switch Loop

var day = 'Saturday';

switch (day) {
  case 'Saturday':
    console.log("This is saturday");
    break;

  case 'Sunday':    
    console.log("This is Holiday");
    break;
    
  case 'Friday':    
    console.log("This is friday");
    break;

  default:
    console.log("This is my default day");
    break;
}

